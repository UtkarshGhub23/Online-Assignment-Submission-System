-- 1. Users Table
CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  email TEXT UNIQUE NOT NULL,
  password_hash TEXT NOT NULL,
  role TEXT NOT NULL CHECK(role IN ('faculty', 'student')),
  avatar_url TEXT DEFAULT NULL,
  phone TEXT DEFAULT NULL,
  address TEXT DEFAULT NULL,
  biography TEXT DEFAULT NULL,
  department TEXT DEFAULT NULL,
  course TEXT DEFAULT NULL,
  semester TEXT DEFAULT NULL,
  section TEXT DEFAULT NULL,
  active SMALLINT NOT NULL DEFAULT 1 CHECK(active IN (0, 1)),
  email_verified SMALLINT NOT NULL DEFAULT 0 CHECK(email_verified IN (0, 1)),
  verification_token TEXT DEFAULT NULL,
  reset_token TEXT DEFAULT NULL,
  reset_token_expiry TIMESTAMP WITH TIME ZONE DEFAULT NULL,
  enrollment_number TEXT UNIQUE DEFAULT NULL,
  notification_prefs JSONB NOT NULL DEFAULT '{"email":true,"in_app":true}'::jsonb,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 2. Courses Table
CREATE TABLE IF NOT EXISTS courses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  code TEXT UNIQUE NOT NULL,
  teacher_id BIGINT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  CONSTRAINT fk_courses_teacher FOREIGN KEY (teacher_id) REFERENCES users(id) ON DELETE CASCADE
);

-- 3. Course Enrollments Table
CREATE TABLE IF NOT EXISTS course_enrollments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  course_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  enrolled_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  CONSTRAINT fk_enrollments_course FOREIGN KEY (course_id) REFERENCES courses(id) ON DELETE CASCADE,
  CONSTRAINT fk_enrollments_student FOREIGN KEY (student_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT uq_course_student UNIQUE(course_id, student_id)
);

-- 4. Assignments Table
CREATE TABLE IF NOT EXISTS assignments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  course_id BIGINT NOT NULL,
  title TEXT NOT NULL,
  subject TEXT DEFAULT NULL,
  department TEXT DEFAULT NULL,
  semester TEXT DEFAULT NULL,
  section TEXT DEFAULT NULL,
  assignment_number TEXT DEFAULT NULL,
  description TEXT DEFAULT NULL,
  detailed_instructions TEXT DEFAULT NULL,
  supporting_documents TEXT DEFAULT NULL,
  reference_materials TEXT DEFAULT NULL,
  due_date TIMESTAMP WITH TIME ZONE NOT NULL,
  max_marks INTEGER NOT NULL DEFAULT 100,
  created_by BIGINT NOT NULL,
  status TEXT NOT NULL DEFAULT 'published' CHECK(status IN ('draft', 'published', 'closed', 'archived')),
  priority TEXT NOT NULL DEFAULT 'medium' CHECK(priority IN ('low', 'medium', 'high')),
  estimated_time TEXT DEFAULT NULL,
  late_allowed SMALLINT NOT NULL DEFAULT 0 CHECK(late_allowed IN (0, 1)),
  max_file_size INTEGER NOT NULL DEFAULT 10,
  allowed_file_types TEXT NOT NULL DEFAULT 'pdf,doc,docx,txt,zip,jpg,png,pptx',
  target_students TEXT DEFAULT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  CONSTRAINT fk_assignments_course FOREIGN KEY (course_id) REFERENCES courses(id) ON DELETE CASCADE,
  CONSTRAINT fk_assignments_creator FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE
);

-- 5. Submissions Table
CREATE TABLE IF NOT EXISTS submissions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assignment_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  file_path TEXT NOT NULL,
  file_name TEXT NOT NULL,
  file_size INTEGER DEFAULT 0,
  submitted_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  status TEXT NOT NULL DEFAULT 'submitted' CHECK(status IN ('not_started', 'in_progress', 'submitted', 'pending_review', 'reviewed', 'approved', 'rejected', 'late_submission', 'missing_submission', 'returned')),
  remarks TEXT DEFAULT NULL,
  submission_id TEXT UNIQUE NOT NULL,
  delay_minutes INTEGER DEFAULT 0,
  CONSTRAINT fk_submissions_assignment FOREIGN KEY (assignment_id) REFERENCES assignments(id) ON DELETE CASCADE,
  CONSTRAINT fk_submissions_student FOREIGN KEY (student_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT uq_assignment_student UNIQUE(assignment_id, student_id)
);

-- 6. Grades Table
CREATE TABLE IF NOT EXISTS grades (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  submission_id BIGINT UNIQUE NOT NULL,
  marks INTEGER NOT NULL,
  feedback TEXT,
  graded_by BIGINT NOT NULL,
  graded_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  is_draft SMALLINT NOT NULL DEFAULT 0 CHECK(is_draft IN (0, 1)),
  CONSTRAINT fk_grades_submission FOREIGN KEY (submission_id) REFERENCES submissions(id) ON DELETE CASCADE,
  CONSTRAINT fk_grades_grader FOREIGN KEY (graded_by) REFERENCES users(id) ON DELETE CASCADE
);

-- 7. Notifications Table
CREATE TABLE IF NOT EXISTS notifications (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  title TEXT NOT NULL,
  message TEXT NOT NULL,
  type TEXT NOT NULL DEFAULT 'info' CHECK(type IN ('info', 'success', 'warning', 'error', 'assignment', 'grade', 'enrollment')),
  is_read SMALLINT NOT NULL DEFAULT 0 CHECK(is_read IN (0, 1)),
  link TEXT DEFAULT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  CONSTRAINT fk_notifications_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- 8. Activity Logs Table
CREATE TABLE IF NOT EXISTS activity_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT DEFAULT NULL,
  action TEXT NOT NULL,
  details TEXT DEFAULT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
  CONSTRAINT fk_activity_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE SET NULL
);

-- 9. System Settings Table
CREATE TABLE IF NOT EXISTS system_settings (
  key TEXT PRIMARY KEY,
  value TEXT NOT NULL
);

-- ====== PERFORMANCE INDEXES ======
CREATE INDEX IF NOT EXISTS idx_users_email ON users(email);
CREATE INDEX IF NOT EXISTS idx_users_role ON users(role);
CREATE INDEX IF NOT EXISTS idx_courses_teacher ON courses(teacher_id);
CREATE INDEX IF NOT EXISTS idx_enrollments_student ON course_enrollments(student_id);
CREATE INDEX IF NOT EXISTS idx_enrollments_course ON course_enrollments(course_id);
CREATE INDEX IF NOT EXISTS idx_assignments_course ON assignments(course_id);
CREATE INDEX IF NOT EXISTS idx_submissions_assignment ON submissions(assignment_id);
CREATE INDEX IF NOT EXISTS idx_submissions_student ON submissions(student_id);
CREATE INDEX IF NOT EXISTS idx_grades_submission ON grades(submission_id);
CREATE INDEX IF NOT EXISTS idx_notifications_user ON notifications(user_id);
CREATE INDEX IF NOT EXISTS idx_notifications_unread ON notifications(user_id, is_read);
CREATE INDEX IF NOT EXISTS idx_activity_logs_user ON activity_logs(user_id);

-- ====== TRIGGERS FOR TIMESTAMPS ======
CREATE OR REPLACE FUNCTION update_modified_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE OR REPLACE TRIGGER update_users_modtime
    BEFORE UPDATE ON users
    FOR EACH ROW
    EXECUTE FUNCTION update_modified_column();

CREATE OR REPLACE TRIGGER update_courses_modtime
    BEFORE UPDATE ON courses
    FOR EACH ROW
    EXECUTE FUNCTION update_modified_column();

CREATE OR REPLACE TRIGGER update_assignments_modtime
    BEFORE UPDATE ON assignments
    FOR EACH ROW
    EXECUTE FUNCTION update_modified_column();


